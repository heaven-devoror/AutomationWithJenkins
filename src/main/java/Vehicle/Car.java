package Vehicle;

public class Car {
    public void move()
    {
        System.out.println("Car is moving forward");
    }

    public void stop()
    {
        System.out.println("Car is stopped");
    }

    public void reverse()
    {
        System.out.println("Car is moving backward");
    }

    public void start()
    {
        System.out.println("Engine starting ....");
    }

    public void turnOff()
    {
        System.out.println("Engine shutting down ....");
    }
}
